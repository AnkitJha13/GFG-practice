class Solution {
    
    // Global variable to track the maximum size of a BST
    public static int maxBST = 0;
    
    // Helper class to store information about a subtree
    static class Info {
        boolean isBST;
        int size;
        int min;
        int max;

        public Info(boolean isBST, int size, int min, int max) {
            this.isBST = isBST;
            this.size = size;
            this.min = min;
            this.max = max;
        }
    }
  
    // Helper function to calculate the largest BST in the tree
    public static Info getLargestBST(Node root) {
        if (root == null) {
            return new Info(true, 0, Integer.MAX_VALUE, Integer.MIN_VALUE);
        }
        
        Info leftInfo = getLargestBST(root.left);
        Info rightInfo = getLargestBST(root.right);
        
        int size = leftInfo.size + rightInfo.size + 1;
        int min = Math.min(root.data, Math.min(leftInfo.min, rightInfo.min));
        int max = Math.max(root.data, Math.max(leftInfo.max, rightInfo.max));
        
        // Check if the current tree is a BST
        if (leftInfo.isBST && rightInfo.isBST && root.data > leftInfo.max && root.data < rightInfo.min) {
            maxBST = Math.max(maxBST, size);
            return new Info(true, size, min, max);
        }
        
        return new Info(false, size, min, max);
    }
    
    // Main function to return the size of the largest BST
    static int largestBst(Node root) {
        maxBST = 0;  // Reset the global variable
        getLargestBST(root);  // Call the helper function
        return maxBST;
    }
}